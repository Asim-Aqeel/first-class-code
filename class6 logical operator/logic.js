"use strict";
//Combining Operators
let result = 3 + 5 * 6;
++result;
console.log(result);
//assignment operators
let number1 = 10;
number1 += 5;
console.log(number1);
//comparison operator (== , !=, >, <)
let num1 = 45;
let num2 = 79;
console.log(num1 == num2);
console.log(num1 != num2);
console.log(num1 > num2);
console.log(num1 < num2);
//logical operators
let a = 5;
let b = 8;
console.log((a > 0) && (b > 0));
console.log((a < 0) || (b > 0));
console.log(!(a > 0));
